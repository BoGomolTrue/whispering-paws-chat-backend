export type BotSpeechGender = "female" | "male";

export function resolveBotGender(
  gender: string | undefined | null,
): BotSpeechGender {
  const g = (gender ?? "female").toLowerCase().trim();
  if (g === "male" || g === "m" || g === "man" || g === "мужской") {
    return "male";
  }
  return "female";
}

