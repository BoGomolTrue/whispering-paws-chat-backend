export function inferReplyGender(text: string): BotSpeechGender | "unknown" {
  const f = (text.match(FEMALE_MARKERS) ?? []).length;
  const m = (text.match(MALE_MARKERS) ?? []).length;
  if (f > m) return "female";
  if (m > f) return "male";
  return "unknown";
}

export function scoreReplyGenderFit(
  text: string,
  botGender: BotSpeechGender,
): number {
  const inferred = inferReplyGender(text);
  if (inferred === "unknown") return 0;
  if (inferred === botGender) return 12;
  return -40;
}

export function isOppositeGender(
  text: string,
  botGender: BotSpeechGender,
): boolean {
  const inferred = inferReplyGender(text);
  return inferred !== "unknown" && inferred !== botGender;
}

